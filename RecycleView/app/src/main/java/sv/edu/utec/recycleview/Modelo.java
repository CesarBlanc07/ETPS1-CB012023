package sv.edu.utec.recycleview;

public class Modelo
{
    private String Nompeli;
    private String Genepeli;
    private String Imgpeli;
    private String Direcpeli;
    private String Yearpeli;
    private String Durapeli;
    private String fechapeli;
    private double Clasipeli;

    public String getNompeli() {
        return Nompeli;
    }

    public void setNompeli(String nompeli) {
        Nompeli = nompeli;
    }

    public String getGenepeli() {
        return Genepeli;
    }

    public void setGenepeli(String genepeli) {
        Genepeli = genepeli;
    }

    public String getImgpeli() {
        return Imgpeli;
    }

    public void setImgpeli(String imgpeli) {
        Imgpeli = imgpeli;
    }

    public String getDirecpeli() {
        return Direcpeli;
    }

    public void setDirecpeli(String direcpeli) {
        Direcpeli = direcpeli;
    }

    public String getYearpeli() {
        return Yearpeli;
    }

    public void setYearpeli(String yearpeli) {
        Yearpeli = yearpeli;
    }

    public String getDurapeli() {
        return Durapeli;
    }

    public void setDurapeli(String durapeli) {
        Durapeli = durapeli;
    }

    public String getFechapeli() {
        return fechapeli;
    }

    public void setFechapeli(String fechapeli) {
        this.fechapeli = fechapeli;
    }

    public double getClasipeli() {
        return Clasipeli;
    }

    public void setClasipeli(double clasipeli) {
        Clasipeli = clasipeli;
    }
}
